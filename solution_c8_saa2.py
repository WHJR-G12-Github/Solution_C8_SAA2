# Defining a function 'power()' with 'a', 'b' as parameters
def power(a,b):
    # Calculating 'result' using power operator
    result = a**b
    # Returning the 'result'
    return result

  
# Calling the function 'power()' with '2' and '3' as arguments 
# and printing the result
print(power(2,3))
